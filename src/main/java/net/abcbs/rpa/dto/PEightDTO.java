package net.abcbs.rpa.dto;

public class PEightDTO {
	
	private String systemCd;
	private String claimNumber;
	private String dateOfService;
	private String serviceLine;
	private String hcpcs;
	private String serviceChargeAmount;
	private String serviceCount;
	private String npi; 
	private String totalCharges; 
	private String revenueCd;
	private String error;
	
	
	//getters and setters
	public String getSystemCd() {
		return systemCd;
	}
	public void setSystemCd(String systemCd) {
		this.systemCd = systemCd;
	}
	public String getClaimNumber() {
		return claimNumber;
	}
	public void setClaimNumber(String claimNumber) {
		this.claimNumber = claimNumber;
	}
	public String getDateOfService() {
		return dateOfService;
	}
	public void setDateOfService(String dateOfService) {
		this.dateOfService = dateOfService;
	}
	public String getServiceLine() {
		return serviceLine;
	}
	public void setServiceLine(String serviceLine) {
		this.serviceLine = serviceLine;
	}
	public String getHcpcs() {
		return hcpcs;
	}
	public void setHcpcs(String hcpcs) {
		this.hcpcs = hcpcs;
	}
	public String getServiceChargeAmount() {
		return serviceChargeAmount;
	}
	public void setServiceChargeAmount(String serviceChargeAmount) {
		this.serviceChargeAmount = serviceChargeAmount;
	}
	public String getServiceCount() {
		return serviceCount;
	}
	public void setServiceCount(String serviceCount) {
		this.serviceCount = serviceCount;
	}
	public String getNpi() {
		return npi;
	}
	public void setNpi(String npi) {
		this.npi = npi;
	}
	public String getTotalCharges() {
		return totalCharges;
	}
	public void setTotalCharges(String totalCharges) {
		this.totalCharges = totalCharges;
	}
	public String getRevenueCd() {
		return revenueCd;
	}
	public void setRevenueCd(String revenueCd) {
		this.revenueCd = revenueCd;
	}
	public String getError() {
		return error;
	}
	public void setError(String error) {
		this.error = error;
	}
	

}
